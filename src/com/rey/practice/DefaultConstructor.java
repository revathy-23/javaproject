package com.rey.practice;

public class DefaultConstructor {
	
		
		String name;
		int age;
		
		DefaultConstructor() {
		
			
		}
		
		public static void main(String[] args) {
			
			DefaultConstructor con=new DefaultConstructor();
			System.out.println(con.name);
			System.out.println(con.age);
			
	}

}
